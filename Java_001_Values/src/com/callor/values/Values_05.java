package com.callor.values;

public class Values_05 {

	public static void main(String[] args) {
	
		/* 
		 * 코드에서 동등연산자(==)
		 * 어떤 값이 같은지?
		 * 값이 같으면 ?????   결과 = true
		 * 값이 다르면 ?????   결과 = false
		 */
		System.out.println(3 == 3); //3하고 3이 같냐? 라는 의미 >> 비교연산
		System.out.println(4 == 3);
		System.out.println(5 == 1); // "==" 같다는 의미 =/= "=" 

		System.out.println(3 >3);
		System.out.println(3 < 3);
		System.out.println(4 > 3);
		System.out.println(4 < 3);
		
		System.out.println(3 >= 3); //부등호 먼저 쓰고 =
		System.out.println(3 <= 3);
		
		System.out.println(4 >= 3);
		System.out.println(4 <= 3);
		
		System.out.println(3 != 3); // = 앞에 ! >> 3과 3이 같지 않냐?
		// != : 값이 서로 같이 아니한가? 
		// 다른 프로그램 (java제외) 에서는 <> 를 쓴다( 3 <>3)
		
		
		
	
	}
}
